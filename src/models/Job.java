package models;

/**
 *
 * @author loisceka
 */
public class Job {

    private String id;
    private String name;
    private int minSalary;
    private int maxSalary;

    public Job() {
    }

    public Job(String id, String name, int minSalary, int maxSalary) {
        this.id = id;
        this.name = name;
        this.minSalary = minSalary;
        this.maxSalary = maxSalary;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMinSalary() {
        return minSalary;
    }

    public void setMinSalary(int minSalary) {
        this.minSalary = minSalary;
    }

    public int getMaxSalary() {
        return maxSalary;
    }

    public void setMaxSalary(int maxSalary) {
        this.maxSalary = maxSalary;
    }

    @Override
    public String toString() {
        return "Job{" + "id=" + id + ", name=" + name + ", minSalary=" + minSalary + ", maxSalary=" + maxSalary + '}';
    }

}
